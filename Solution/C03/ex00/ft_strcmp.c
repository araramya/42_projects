int		ft_strcmp(char *s1, char *s2)
{
	int index;

	index = 0;
	while (s2[index] == s1[index])
	{
		if (s1[index] == '\0')
		{
			return (0);
		}
		index++;
	}
	return ((int)s1[index] - (int)s2[index]);
}
